package JAVA_2_9_casting;

public class Explicit_Casting {
public static void main(String[] args) {
	int i=(int)55.5;//(int)---> It is called as type cast operator which is externally added by developers or by test engineers
	System.out.println(i);   // output is --> 55 means 0.5 data loss 
	
	// Converting higher order data type to lower order data type is called as Explicit Cating..
	// Here we are adding type cast operator
}
}
