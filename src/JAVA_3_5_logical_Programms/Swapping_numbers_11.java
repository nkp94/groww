package JAVA_3_5_logical_Programms;

public class Swapping_numbers_11 {
public static void main(String[]args) {
	int d=4;
	int e=46;
	System.out.println(" Before Swap d is :" +d);
	System.out.println(" Before Swap e is :" +e);
	int f=d;
	d=e;
	e=f;
	System.out.println(" After Swapping d is :" +d);
	System.out.println(" After Swapping e is :" +e);
}
}
